public class loop {
    public static void main(String[] args) {
        System.out.println("su khac biet giua while va do while");
        int i = 9;
        while (i < 6) {
            System.out.println(i);
            i++;
        }
        System.out.println("====================");
        int k = 9; // se chay it nhat 1 lan neu k dung dk
        do {
            System.out.println(k);
            k++;
        } while (k < 6);
    }
}
